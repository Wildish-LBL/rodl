package pl.psnc.dl.wf4ever.notifications;

/**
 * Type of action which invokes a new entry creation operation.
 */
public enum ActionType {
    /** In case of new Research Object created. */
    NEW_RO,
    /** In case of Research Object updated. */
    UPDATED_RO,
    /** In case of Research Object deleted. */
    DELETED_RO
}
